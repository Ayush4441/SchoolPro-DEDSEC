class dot_loading_anime
{
    public static void main()throws InterruptedException
    {
        
        for(int i = 0;i<5;i++)
        {
            System.out.print("......");
            Thread.sleep(700);
            System.out.print('\u000C');
            System.out.print("o.....");
            Thread.sleep(700);
            System.out.print('\u000C');
            System.out.print(".o....");
            Thread.sleep(700);
            System.out.print('\u000C');
            System.out.print("..o...");
            Thread.sleep(700);
            System.out.print('\u000C');
            System.out.print("...o..");
            Thread.sleep(700);
            System.out.print('\u000C');
            System.out.print("....o.");
            Thread.sleep(700);
            System.out.print('\u000C');
            System.out.print(".....o");
            Thread.sleep(700);
            System.out.print('\u000C');
        }
        System.out.print('\u000C');
    }
}
